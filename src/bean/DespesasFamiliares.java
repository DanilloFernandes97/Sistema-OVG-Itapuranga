    /**
     * @return the farmacia
     */
    public double getFarmacia() {
        return farmacia;
    }

    /**
     * @param farmacia the farmacia to set
     */
    public void setFarmacia(double farmacia) {
        this.farmacia = farmacia;
    }

    /**
     * @return the gas
     */
    public double getGas() {
        return gas;
    }

    /**
     * @param gas the gas to set
     */
    public void setGas(double gas) {
        this.gas = gas;
    }

